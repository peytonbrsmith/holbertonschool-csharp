using System;

namespace MyMath
{
    /// <summary>
    /// Class of math operations
    /// </summary>
    public class Operations
    {
        /// <summary>
        /// Add function
        /// </summary>
        /// <param name="a"></param>
        /// <param name="b"></param>
        /// <returns> sum of a and b </returns>
        public static int Add(int a, int b)
        {
            return a + b;
        }
    }
}
